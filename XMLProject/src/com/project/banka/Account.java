package com.project.banka;

public class Account {
	
	private int id;
	private Owner owner;
	private String number;
	
	public Account(Owner owner, String number) {
		super();
		this.owner = owner;
		this.number = number;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Owner getOwner() {
		return owner;
	}

	public void setOwner(Owner owner) {
		this.owner = owner;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	@Override
	public String toString() {
		return "Account [id=" + id + ", owner=" + owner + ", number=" + number
				+ "]";
	}
	
}
