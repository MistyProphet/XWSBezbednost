package com.project.banka;

public class Company extends Owner {

	private String id;
	private String name;
	private String pib;
	
	public Company(String name, String pib) {
		super();
		this.name = name;
		this.pib = pib;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPib() {
		return pib;
	}

	public void setPib(String pib) {
		this.pib = pib;
	}

	@Override
	public String toString() {
		return "Company [id=" + id + ", name=" + name + ", pib=" + pib + "]";
	}
	
}
